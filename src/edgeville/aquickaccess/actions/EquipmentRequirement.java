package edgeville.aquickaccess.actions;

public class EquipmentRequirement {
	private int level;
	private int skill;
	
	public EquipmentRequirement(int level, int skill) {
		this.level = level;
		this.skill = skill;
	}

	public int getLevel() {
		return level;
	}

	public int getSkill() {
		return skill;
	}
	
	
}
